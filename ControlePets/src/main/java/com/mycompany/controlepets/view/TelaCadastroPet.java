    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
    if (jTable1.getSelectedRow() != -1) {
        jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        jTextField2.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
        jTextField3.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
        jTextField4.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
    }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
    if (jTable1.getSelectedRow() != -1) {
        int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        PetDAO dao = new PetDAO();
        dao.delete(id);
        limparCampos();
        readJTable();
    } else {
        JOptionPane.showMessageDialog(null, "Selecione um pet para excluir.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    if (jTable1.getSelectedRow() != -1) {
        Pet pet = new Pet();
        PetDAO dao = new PetDAO();
        pet.setId(Integer.parseInt(jTextField1.getText()));
        pet.setNomePet(jTextField2.getText());
        pet.setRaca(jTextField3.getText());
        pet.setNomeTutor(jTextField4.getText());
        dao.update(pet);
        limparCampos();
        readJTable();
    } else {
        JOptionPane.showMessageDialog(null, "Selecione um pet para atualizar.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

