private void limparCampos() {
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    jTextField2.requestFocus();
}


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
if (jTextField2.getText().isEmpty() || jTextField4.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nome do Pet e do Tutor são obrigatórios.");
        return;
    }
    Pet pet = new Pet();
    PetDAO dao = new PetDAO();
    pet.setNomePet(jTextField2.getText()); // txtNomePet -> jTextField2
    pet.setRaca(jTextField3.getText());    // txtRaca -> jTextField3
    pet.setNomeTutor(jTextField4.getText()); // txtNomeTutor -> jTextField4
    dao.create(pet);
    limparCampos();
    readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed

